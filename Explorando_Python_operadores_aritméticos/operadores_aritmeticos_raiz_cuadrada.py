import math
print(math.sqrt(16))

# La raíz cuadrada de un número no es más que el número
# elevado a 0,5. La función pow() en Python devuelve el 
# valor de un número elevado a una potencia especificada
# y se puede usar para calcular la raíz cuadrada de un 
# número como se muestra a continuación. El primer 
# parámetro de la función pow() es la base y el segundo 
# es el exponente.

print(pow(36,0.5))